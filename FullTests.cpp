bool testGetWorstModels1() {
    bool result = true;
    void* DS = Init();
    AddCarType(DS, 10, 5);
    for(int j = 0; j < 5; j++) for(int i = 0; i < j; i++) SellCar(DS, 10, j);
    int* types = new int[5];
    int* models = new int[5];
    StatusType s = GetWorstModels(DS, 5, types, models);
    ASSERT(s == SUCCESS);
    for(int i = 0; i < 5; i++) {
        ASSERT(types[i] == 10);
        ASSERT(models[i] == i);
    }
    s = GetWorstModels(DS, 6, types, models);
    ASSERT(s == FAILURE);
    s = GetWorstModels(nullptr, 5, types, models);
    ASSERT(s == INVALID_INPUT);
    s = GetWorstModels(DS, 0, types, models);
    ASSERT(s == INVALID_INPUT);
    s = GetWorstModels(DS, -1, types, models);
    ASSERT(s == INVALID_INPUT);
    delete[] types;
    delete[] models;
    Quit(&DS);
    return result;
}

